#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t nn;
	size_t i;

	nn = ft_strlen(s1);
	i = 0;
	while (s2[i] && i < n)
	{
		s1[nn + i] = s2[i];
		i++;
	}
	s1[nn + i] = '\0';
	return (s1);
}
